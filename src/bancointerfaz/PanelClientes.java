    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        Cliente c = new Cliente();
    c.setNombres(txtNombre.getText());
    c.setaPaterno(txtApellido.getText());
    c.setaMaterno(""); // opcional
    c.setDni(txtDocumento.getText());
    c.setTipoDocumentoId(cmbTipoDocumento.getSelectedIndex() + 1);
    c.setGeneroId(cmbGenero.getSelectedIndex() + 1);

    ClienteDAO dao = new ClienteDAO();
    if (dao.insertar(c)) {
        JOptionPane.showMessageDialog(this, "Cliente guardado correctamente â");
        cargarClientes(); // refresca la tabla
        limpiarCampos();
    } else {
        JOptionPane.showMessageDialog(this, "Error al guardar cliente â");
    }

    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
          int fila = tablaClientes.getSelectedRow();
    if (fila == -1) {
        JOptionPane.showMessageDialog(this, "Selecciona un cliente de la tabla primero â ï¸");
        return;
    }

    int id = (int) tablaClientes.getValueAt(fila, 0);

    Cliente c = new Cliente();
    c.setId(id);
    c.setNombres(txtNombre.getText());
    c.setaPaterno(txtApellido.getText());
    c.setaMaterno("");
    c.setDni(txtDocumento.getText());
    c.setTipoDocumentoId(cmbTipoDocumento.getSelectedIndex() + 1);
    c.setGeneroId(cmbGenero.getSelectedIndex() + 1);

    ClienteDAO dao = new ClienteDAO();
    if (dao.actualizar(c)) {
        JOptionPane.showMessageDialog(this, "Cliente actualizado correctamente â");
        cargarClientes();
        limpiarCampos();
    } else {
        JOptionPane.showMessageDialog(this, "Error al actualizar â");
    }
    }//GEN-LAST:event_btnActualizarActionPerformed

